#include<iostream>
using namespace std;
int main()
{
    int a,b;
    a=30;
    b=10;
    cout<<"Your sum is: "<<(a+b)<<endl;
    cout<<"Your sum is: "<<(a-b)<<endl;
    a++;
    b*=a;
    cout<<"Your sum is: "<<(a*b)<<endl;
    ++a;
    ++b;    ;
    cout<<"Your sum is: "<<(a/b)<<endl;
}
